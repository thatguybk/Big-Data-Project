quantityProduced_df["Date"] = pd.to_datetime(quantityProduced_df["Date"], errors="coerce")
quantityProduced_df["Date"] = pd.to_datetime(quantityProduced_df["Date"]).dt.to_period("M").dt.to_timestamp()
